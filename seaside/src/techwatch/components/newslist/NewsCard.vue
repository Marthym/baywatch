<template>
  <div class="flex flex-col lg:flex-row bg-gray-600 shadow rounded-lg"
       :class="{
        'shadow-lg lg:h-60 my-8 border border-gray-400': card.isActive,
        'lg:h-56 m-5': !card.isActive,
        'opacity-30': card.data.state?.read && !card.isActive,
       }" @click="$emit('activate')">

    <figure class="flex-none">
      <img :src="card.data.image"
           :srcset="card.srcset"
           :sizes="card.sizes"
           class="w-full h-24 lg:h-full lg:w-60 object-cover rounded-t-lg lg:rounded-none lg:rounded-l-lg
              bg-no-repeat bg-cover lg:bg-contain bg-center
              bg-[url('/placeholder.svg')] text-transparent"
           loading="lazy"
           :alt="card.data.title"/>
    </figure>

    <div :class="{ 'm-6': card.isActive, 'm-4': !card.isActive}" class="flex-grow">
      <div class="flex flex-col h-full overflow-hidden">
        <a class="font-semibold text-xl" target="_blank" :href="card.data.link" :title="card.data.link"
           v-html="card.data.title"></a>
        <span v-html="card.data.description" class="mt-2 text-base flex-grow overflow-hidden"></span>

        <div class="flex flex-row flex-wrap-reverse items-center justify-end items-end text-xs mt-2">
          <slot name="actions"></slot>
          <span class="grow"></span>
          <div class="italic text-right whitespace-nowrap">
            <button class="badge badge-xs p-2 rounded" @click.stop="$emit('addFilter', {type: 'feed', entity: f})"
                    v-for="f in card.data.feeds">{{ f.name }}
            </button>
            <div class="self-end">{{ publication }}</div>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script lang="ts">
import {Options, Prop, Vue} from 'vue-property-decorator';
import {NewsView} from "@/techwatch/components/newslist/model/NewsView";

@Options({name: 'NewsCard', emits: ['addFilter']})
export default class NewsCard extends Vue {
  @Prop() card?: NewsView;

  get publication(): string | undefined {
    if (this.card) {
      return new Date(this.card.data.publication).toLocaleDateString(navigator.languages, {
        timeZone: Intl.DateTimeFormat().resolvedOptions().timeZone,
        year: 'numeric', month: 'short', day: '2-digit', hour: "2-digit", minute: "2-digit"
      });
    } else {
      return undefined;
    }
  }
}
</script>